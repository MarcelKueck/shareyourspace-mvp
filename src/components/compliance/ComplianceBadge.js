import React from 'react';
import { complianceStandards, verificationLevels } from '../../services/complianceService';

/**
 * Displays a badge for a compliance standard with verification level
 */
const ComplianceBadge = ({ standardId, level = 'self-declared', size = 'md', showTooltip = false }) => {
  const standard = complianceStandards[standardId] || null;
  const verification = verificationLevels[level] || verificationLevels['self-declared'];
  
  if (!standard) {
    return null;
  }
  
  // Determine badge sizes
  const sizes = {
    sm: 'h-5 w-5 text-xs',
    md: 'h-6 w-6 text-sm',
    lg: 'h-8 w-8 text-base'
  };
  
  // Determine badge colors
  const colors = {
    yellow: 'bg-yellow-100 dark:bg-yellow-900 text-yellow-800 dark:text-yellow-200 border-yellow-200 dark:border-yellow-800',
    blue: 'bg-blue-100 dark:bg-blue-900 text-blue-800 dark:text-blue-200 border-blue-200 dark:border-blue-800',
    green: 'bg-green-100 dark:bg-green-900 text-green-800 dark:text-green-200 border-green-200 dark:border-green-800',
    purple: 'bg-purple-100 dark:bg-purple-900 text-purple-800 dark:text-purple-200 border-purple-200 dark:border-purple-800'
  };
  
  const badgeColor = colors[verification.color] || colors.yellow;
  const badgeSize = sizes[size] || sizes.md;
  
  return (
    <div className="relative inline-flex flex-col items-center group">
      <div className={`flex items-center justify-center rounded-full ${badgeColor} p-1 border`}>
        <span className="sr-only">{standard.name}</span>
        {getIconForStandard(standard.icon, badgeSize)}
      </div>
      {showTooltip && (
        <div className="absolute bottom-full mb-2 hidden group-hover:block z-10">
          <div className="bg-white dark:bg-dark-bg-light shadow-lg rounded-md border border-gray-200 dark:border-dark-bg p-2 w-48 text-xs">
            <div className="font-medium text-gray-900 dark:text-dark-text-primary">{standard.name}</div>
            <div className="text-gray-600 dark:text-dark-text-secondary mt-1">{standard.description}</div>
            <div className={`mt-1 font-medium ${verification.color === 'yellow' ? 'text-yellow-600 dark:text-yellow-400' : 
                                               verification.color === 'blue' ? 'text-blue-600 dark:text-blue-400' :
                                               verification.color === 'green' ? 'text-green-600 dark:text-green-400' :
                                               'text-purple-600 dark:text-purple-400'}`}>
              {verification.name}
            </div>
          </div>
        </div>
      )}
    </div>
  );
};

